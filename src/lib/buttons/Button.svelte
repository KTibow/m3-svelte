  type Props = {
    type: "elevated" | "filled" | "tonal" | "outlined" | "text";
    iconType?: "none" | "left" | "full";
    children?: Snippet;
  } & ActionProps;

  let props: Props = $props();
</script>

{#if "href" in props}
  {@const { type, href, iconType = "none", children, ...extra } = props}
